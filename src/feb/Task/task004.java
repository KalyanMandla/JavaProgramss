package feb.Task;

public class task004 {
    public static void main(String[] args) {

        int n = 10;
        System.out.println("Multiplication Table for " + n + " is: ");
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + (n*i));

        }
    }
}
