package feb.ex_19022025;

public class Lab005_DataTypes {
    //Data Types
    //Boolean Type
    //Numerical Type
    //Integral - byte, short, int, long, char.
    //size - how much they can store
    public static void main(String[] args){

        //Primitive Data Types
        byte b = 127; //8 bits, 1 byte
        short s = 128; //16 bits, 2 byte
        int i = 1000; //32 bits, 4 byte
        long phone_number = 9870654320L; //64 bits, 8 bytes
        long phone_number2 = 9870654320l; //64 bits, 8 bytes

        char grade = 'A'; // 'A', b, c, d, e -> ASCII value - int - assigned
        //16 bits, 2 byte

        float f = 3.14f;
        float f2 = 3.14F; // 32 bits, 4 byte

        double d = 118.796332; // 64 bits, 8 bytes

        byte age = 123;
        System.out.println(age);

        //Non-Primitive Data Types(user-defined)
        String name = "Kalyan"; // string - bunch of char(collection of chars)
        //Array, Class-User Data Types.


    }
}
