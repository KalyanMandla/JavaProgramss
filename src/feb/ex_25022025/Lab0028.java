public class Lab0028 {
    public static void main(String[] args) {
        //char c = 'a' -> Vowel or Consonant
        //vowel -> a, e, i, o, u
        char ch = 'a';
        if( ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u'){
            System.out.println("Vowel");
        }
        else{
            System.out.println("Consonant");
        }

    }
}
